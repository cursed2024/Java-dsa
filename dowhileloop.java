class dowhileloop{
    public static void main(String args[]){
        System.out.print("Example for do while loop \n");
        int i = 1;
        do{
            i++;
            System.out.println(i);
            System.out.print("is less than 10 \n");
            System.out.print("Incrementing i \n");
        }while(i<10);
    }
}